/* Query 15:
   How is revenue distributed across customer groups based on the Pareto principle?
*/

WITH CustomerRevenue AS
(
    SELECT
        Customer_Id,
        SUM(Sales) AS Revenue
    FROM supply_chain_cleaned
    GROUP BY Customer_Id
),

RankedCustomers AS
(
    SELECT
        Customer_Id,
        Revenue,
        NTILE(5) OVER (ORDER BY Revenue DESC) AS Revenue_Group
    FROM CustomerRevenue
),

Pareto AS
(
    SELECT
        Revenue_Group,
        COUNT(*) AS Customers,
        SUM(Revenue) AS Revenue
    FROM RankedCustomers
    GROUP BY Revenue_Group
)

SELECT
CASE Revenue_Group
    WHEN 1 THEN 'Top 20%'
    WHEN 2 THEN 'Next 20%'
    WHEN 3 THEN 'Middle 20%'
    WHEN 4 THEN 'Lower 20%'
    WHEN 5 THEN 'Bottom 20%'
END AS Customer_Group,

Customers,

ROUND(Revenue,2) AS Revenue,

ROUND(
    Revenue * 100.0 /
    SUM(Revenue) OVER(),
    2
) AS Revenue_Percentage

FROM Pareto
ORDER BY Revenue_Group;




/* Query 16: Revenue Recovery Opportunity Analysis
   Which markets offer the greatest revenue recovery opportunity by reducing late deliveries?
*/

SELECT

    Market,

    COUNT(Order_Id) AS Total_Orders,

    SUM(CASE
            WHEN Late_delivery_risk = 1
            THEN 1
            ELSE 0
        END) AS Late_Orders,

    ROUND(
        SUM(CASE
                WHEN Late_delivery_risk = 1
                THEN Sales
                ELSE 0
            END),
        2
    ) AS Revenue_At_Risk,

    ROUND(
        SUM(CASE
                WHEN Late_delivery_risk = 1
                THEN Sales
                ELSE 0
            END) * 0.10,
        2
    ) AS Recoverable_Revenue

FROM supply_chain_cleaned

GROUP BY Market

ORDER BY Recoverable_Revenue DESC;




