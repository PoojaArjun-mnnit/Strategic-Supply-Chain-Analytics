/* Query 1: Discount Band Profitability Analysis
   How do different discount bands impact sales, profit, and profit margins?
*/

SELECT
    CASE
        WHEN Order_Item_Discount_Rate = 0 THEN 'No Discount'
        WHEN Order_Item_Discount_Rate <= 0.10 THEN '0–10%'
        WHEN Order_Item_Discount_Rate <= 0.20 THEN '10–20%'
        ELSE '20%+'
    END AS Discount_Band,

    COUNT(*) AS Total_Orders,

    ROUND(AVG(Sales),2) AS Avg_Sales,

    ROUND(AVG(Order_Profit_Per_Order),2) AS Avg_Profit,

    ROUND(AVG(Profit_Margin),2) AS Avg_Profit_Margin

FROM supply_chain_cleaned

GROUP BY
CASE
    WHEN Order_Item_Discount_Rate = 0 THEN 'No Discount'
    WHEN Order_Item_Discount_Rate <= 0.10 THEN '0–10%'
    WHEN Order_Item_Discount_Rate <= 0.20 THEN '10–20%'
    ELSE '20%+'
END

ORDER BY Avg_Profit;

/* Query 2:
   How much revenue is generated by repeat customers compared to first-time customers?
   */

   WITH Customer_Summary AS
(
    SELECT
        Customer_Id,
        COUNT(Order_Id) AS Total_Orders,
        SUM(Sales) AS Revenue
    FROM supply_chain_cleaned
    GROUP BY Customer_Id
)

SELECT
    CASE
        WHEN Total_Orders > 1 THEN 'Repeat Customer'
        ELSE 'First-Time Customer'
    END AS Customer_Type,

    COUNT(Customer_Id) AS Total_Customers,

    ROUND(SUM(Revenue),2) AS Total_Revenue,

    ROUND(AVG(Revenue),2) AS Avg_Revenue_Per_Customer

FROM Customer_Summary

GROUP BY
CASE
    WHEN Total_Orders > 1 THEN 'Repeat Customer'
    ELSE 'First-Time Customer'
END

ORDER BY Total_Revenue DESC;


/* Query 3: Revenue at Risk by Region
   Which regions have the highest revenue exposed to delivery risk?
*/

SELECT
    TOP 5 Order_Region,

    COUNT(*) AS Total_Orders,

    ROUND(SUM(Revenue_at_Risk),2) AS Total_Revenue_At_Risk,

    ROUND(AVG(Revenue_at_Risk),2) AS Avg_Revenue_At_Risk,

    ROUND(SUM(Sales),2) AS Total_Sales

