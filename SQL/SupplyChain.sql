ORDER BY Fraud_Exposure_Value DESC;



/* Query 13: Revenue Lost Due to Failed Orders
   Which product categories experience the highest revenue loss due to cancelled or incomplete orders?
*/

SELECT
    Category_Name,
    SUM(
        CASE
            WHEN Order_Status IN ('CANCELED','PENDING_PAYMENT','PAYMENT_REVIEW')
            THEN Sales
            ELSE 0
        END
    ) AS Lost_Revenue,

    SUM(Sales) AS Total_Revenue,

    ROUND(
        SUM(
            CASE
                WHEN Order_Status IN ('CANCELED','PENDING_PAYMENT','PAYMENT_REVIEW')
                THEN Sales
                ELSE 0
            END
        ) * 100.0 /
        NULLIF(SUM(Sales),0),
        2
    ) AS Revenue_Loss_Percentage

FROM supply_chain_cleaned
GROUP BY Category_Name
ORDER BY Revenue_Loss_Percentage DESC;




/* Query 14: High-Value At-Risk Customers
