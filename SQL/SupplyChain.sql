    CASE
        WHEN Order_Status IN ('COMPLETE','CLOSED')
            THEN 'Completed'

        WHEN Order_Status IN ('PROCESSING','PENDING','ON_HOLD')
            THEN 'Operational Pipeline'

        WHEN Order_Status='PENDING_PAYMENT'
            THEN 'Pending Payment'

        WHEN Order_Status='SUSPECTED_FRAUD'
            THEN 'Fraud Risk'

        WHEN Order_Status='CANCELED'
            THEN 'Cancelled'

        ELSE 'Others'
    END AS Business_Status,

    COUNT(*) AS Total_Orders,

    ROUND(SUM(Sales),2) AS Revenue,

    ROUND(
        SUM(Sales)*100.0/
        (SELECT SUM(Sales) FROM supply_chain_cleaned),2
    ) AS Revenue_Percentage

FROM supply_chain_cleaned

GROUP BY
CASE
        WHEN Order_Status IN ('COMPLETE','CLOSED')
            THEN 'Completed'

        WHEN Order_Status IN ('PROCESSING','PENDING','ON_HOLD')
            THEN 'Operational Pipeline'

        WHEN Order_Status='PENDING_PAYMENT'
            THEN 'Pending Payment'

        WHEN Order_Status='SUSPECTED_FRAUD'
            THEN 'Fraud Risk'

        WHEN Order_Status='CANCELED'
            THEN 'Cancelled'

        ELSE 'Others'
END

ORDER BY Revenue DESC;




/* Query 12: Fraud Exposure Analysis
   Which payment methods and markets contribute the highest financial fraud exposure?
*/

SELECT
    Type AS Payment_Type,
    Market,
    COUNT(Order_Id) AS Fraud_Order_Count,
    ROUND(SUM(Sales),2) AS Fraud_Exposure_Value,
    ROUND(AVG(Sales),2) AS Avg_Order_Value,
