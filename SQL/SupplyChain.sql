/* Query 5: Category-wise Delay Rate Analysis
   Which product categories experience the highest delayed delivery rate?
*/

SELECT 
   TOP 5 Category_Name,
    COUNT(*) AS Total_Orders,
    SUM(CASE WHEN Delay_Days > 0 THEN 1 ELSE 0 END) AS Delayed_Orders,
    ROUND(
        100.0 * SUM(CASE WHEN Delay_Days > 0 THEN 1 ELSE 0 END) / COUNT(*),
        2
    ) AS Delay_Rate
FROM supply_chain_cleaned
GROUP BY Category_Name
HAVING COUNT(*) >= 1000
ORDER BY Delay_Rate DESC;




/* Query 6: Revenue per Customer by Region
   Which regions have a large customer base but generate lower revenue per customer, indicating opportunities for revenue growth?
*/

SELECT
    TOP 5
    Order_Region,

    COUNT(DISTINCT Customer_Id) AS Total_Customers,

    ROUND(SUM(Sales),2) AS Total_Sales,

    ROUND(
        SUM(Sales) * 1.0 / COUNT(DISTINCT Customer_Id),
        2
    ) AS Revenue_Per_Customer,

    ROUND(SUM(Order_Profit_Per_Order),2) AS Total_Profit

FROM supply_chain_cleaned

GROUP BY Order_Region

HAVING COUNT(DISTINCT Customer_Id) >= 100

ORDER BY Revenue_Per_Customer ASC;




